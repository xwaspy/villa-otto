export const t = (lang, key) => {
  const keys = key.split(".");
  let result = translations[lang];

  for (const k of keys) {
    if (result && typeof result === "object" && result[k] !== undefined) {
      result = result[k];
    } else {
      // Fallback to English if key is not found
      let fallbackResult = translations.en;
      for (const fallbackKey of keys) {
        if (
          fallbackResult &&
          typeof fallbackResult === "object" &&
          fallbackResult[fallbackKey] !== undefined
        ) {
          fallbackResult = fallbackResult[fallbackKey];
        } else {
          // If even English key is not found, return the key itself
          return key;
        }
      }
      return fallbackResult;
    }
  }

  return result;
};

export default translations;
